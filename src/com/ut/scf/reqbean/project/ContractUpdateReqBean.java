package com.ut.scf.reqbean.project;

import com.ut.scf.reqbean.PageReqBean;

public class ContractUpdateReqBean extends PageReqBean {

	/**
	 * 签约日期
	 */
	private String contractNo;

	private String signDate;

	/**
	 * 合同生效日期
	 */

	private String contractValDate;

	/**
	 * 合同到期日期
	 */

	private String contractDueDate;

	/**
	 * 附件数
	 */
	private String fileNum;

	/**
	 * 附件信息
	 */
	private String fileInfo;

	public String getContractNo() {
		return contractNo;
	}

	public void setContractNo(String contractNo) {
		this.contractNo = contractNo;
	}

	public String getSignDate() {
		return signDate;
	}

	public void setSignDate(String signDate) {
		this.signDate = signDate;
	}

	public String getContractValDate() {
		return contractValDate;
	}

	public void setContractValDate(String contractValDate) {
		this.contractValDate = contractValDate;
	}

	public String getContractDueDate() {
		return contractDueDate;
	}

	public void setContractDueDate(String contractDueDate) {
		this.contractDueDate = contractDueDate;
	}

	public String getFileNum() {
		return fileNum;
	}

	public void setFileNum(String fileNum) {
		this.fileNum = fileNum;
	}

	public String getFileInfo() {
		return fileInfo;
	}

	public void setFileInfo(String fileInfo) {
		this.fileInfo = fileInfo;
	}

}
