package com.ut.scf.reqbean.finance;

import com.ut.scf.reqbean.BaseReqBean;

public class RecManageAddReqBean extends BaseReqBean {
	/**
	 * 所属企业id
	 */
	private String corpId;
	/**
	 * 项目名字
	 */
	private String projectName;
	/**
	 * 合同编号
	 */
	private String contractNo;
	/**
	 * 结算账户
	 */
	private String balanceAccount;
	/**
	 * 费用类型
	 */
	private String expenseType;
	/**
	 * 付款金额
	 */
	private Double payAmount;
	/**
	 * 实还金额
	 */
	private Double actualAmount;
	/**
	 * 应还金额
	 */
	private	Double shouldRepayAmount;
	public String getCorpId() {
		return corpId;
	}
	public void setCorpId(String corpId) {
		this.corpId = corpId;
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getContractNo() {
		return contractNo;
	}
	public void setContractNo(String contractNo) {
		this.contractNo = contractNo;
	}
	public String getBalanceAccount() {
		return balanceAccount;
	}
	public void setBalanceAccount(String balanceAccount) {
		this.balanceAccount = balanceAccount;
	}
	public String getExpenseType() {
		return expenseType;
	}
	public void setExpenseType(String expenseType) {
		this.expenseType = expenseType;
	}
	public Double getPayAmount() {
		return payAmount;
	}
	public void setPayAmount(Double payAmount) {
		this.payAmount = payAmount;
	}
	public Double getActualAmount() {
		return actualAmount;
	}
	public void setActualAmount(Double actualAmount) {
		this.actualAmount = actualAmount;
	}
	public Double getShouldRepayAmount() {
		return shouldRepayAmount;
	}
	public void setShouldRepayAmount(Double shouldRepayAmount) {
		this.shouldRepayAmount = shouldRepayAmount;
	}
	
}
