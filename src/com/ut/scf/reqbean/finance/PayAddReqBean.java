package com.ut.scf.reqbean.finance;

import java.math.BigDecimal;

import javax.validation.constraints.Pattern;

import com.ut.scf.core.dict.ScfConsts;
import com.ut.scf.reqbean.BaseReqBean;

/**
 * 
 * @author changxin
 *
 */
public class PayAddReqBean extends BaseReqBean{
	private String corpId;
	
	private String projectName;
	
	private String payNumber;

	@Pattern(regexp = ScfConsts.REGEX_DATE, message = "{date.regexp.notpattern}")
	private String payDate;
	
	private String payPeo;
	
	private int payStatus;
	
	private BigDecimal payAmount;
	
	private String contractNo;
	
	private int payType;
	
	private String payDocument;

	public String getCorpId() {
		return corpId;
	}

	public void setCorpId(String corpId) {
		this.corpId = corpId;
	}

	public String getProjectName() {
		return projectName;
	}

	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}

	public String getPayNumber() {
		return payNumber;
	}

	public void setPayNumber(String payNumber) {
		this.payNumber = payNumber;
	}

	public String getPayDate() {
		return payDate;
	}

	public void setPayDate(String payDate) {
		this.payDate = payDate;
	}

	public String getPayPeo() {
		return payPeo;
	}

	public void setPayPeo(String payPeo) {
		this.payPeo = payPeo;
	}

	public int getPayStatus() {
		return payStatus;
	}

	public void setPayStatus(int payStatus) {
		this.payStatus = payStatus;
	}

	public BigDecimal getPayAmount() {
		return payAmount;
	}

	public void setPayAmount(BigDecimal payAmount) {
		this.payAmount = payAmount;
	}

	public String getContractNo() {
		return contractNo;
	}

	public void setContractNo(String contractNo) {
		this.contractNo = contractNo;
	}

	public int getPayType() {
		return payType;
	}

	public void setPayType(int payType) {
		this.payType = payType;
	}

	public String getPayDocument() {
		return payDocument;
	}

	public void setPayDocument(String payDocument) {
		this.payDocument = payDocument;
	}
	
	
	

}
